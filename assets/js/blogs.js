const displayPageNav = perPage => {
  
  let pagination =``
  const totalItems = blog.length
  perPage = perPage ? perPage : 1
  const pages = Math.ceil(totalItems/perPage)
  
  for(let i = 1; i <= pages; i++) {
    pagination += `<a href="#" onClick="displayItems(${i},${perPage})" id="pb${i}">${i}</a>`
  }

  document.getElementById('pagination').innerHTML = pagination;
}

 


const displayItems = ( page = 1, perPage = 2 ) => {

    if (page == 1 || page <=0) {
        document.getElementById("pb1").classList.add("active");
        document.getElementById("pb2").classList.remove("active");
    } if (page == 2 || page <=0) {
        document.getElementById("pb1").classList.remove("active");
        document.getElementById("pb2").classList.add("active");
    };
  
 let index, offSet
  
  if(page == 1 || page <=0)  {
    index = 0
    offSet = perPage
  } else if(page > blog.length) {
    index = page - 1
    offSet = blog.length
  } else {
    index = page * perPage - perPage
    offSet = index + perPage
  }
  
  const slicedItems = blog.slice(index, offSet)
  
  const blogcard = slicedItems.map(item => 
   `
    <div class="card">
        <div class="blog-img">
            <a href="blogpost.html">
                <img src=${item.blogImg}>
            </a> 
        </div>
        <div class="blog-content">
            <div class="blog-author">
                <img src=${item.avatar}>
                <h4>${item.name}</h4>
            </div>
            <p><i class="fa-solid fa-calendar-days"></i> ${item.date}</p>
            <h3>${item.title}</h3>
            <p>${item.article.substring(0, 120) + "..."}</p>
        </div>
    </div>
    `)
  
  document.getElementById('blog-container').innerHTML = blogcard.join('');
 
}

let perPage = 8;
displayPageNav(perPage);
displayItems(1, perPage);